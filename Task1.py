def skl(n):
    if n[-2:] == '2' or n[-2:] == '3' or n[-2:] == '4':
        print(f"{n} компьютера")
    elif n[-2:] == '12' or n[-2:] == '13' or n[-2:] == '14':
        print(f"{n} компьютеров")
    elif n[-2:] == '1' or n[-2:] == '21' or n[-2:] == '31' or n[-2:] == '41' or n[-2:] == '51' or \
            n[-2:] == '61' or n[-2:] == '71' or n[-2:] == '81' or n[-2:] == '91':
        print(f"{n} компьютер")
    else:
        print(f"{n} компьютеров")


n1 = input()
skl(n1)
